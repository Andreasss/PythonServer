from __future__ import print_function
import Pyro4

@Pyro4.expose
class theLibrary(object):

    #Initialize the list
    def __init__(self):
        self.contents = []

    #Returns the list
    def list_contents(self):
        return self.contents
    
    #Appends a book to the list.
    def store(self, row):
        self.contents.append(row)
        print("\n{} was stored in library.".format(row))

# Main function
def main():
    library = theLibrary()
    Pyro4.Daemon.serveSimple(
        {
            library: "example.library"
        },
        ns=True)


if __name__ == "__main__":
    main()

